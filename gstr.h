/*
 * Counts non-overlapping occurrences of needle in the string.
 */
static inline size_t gstrcount(const char *s, size_t len, const char *needle,
                               size_t n_len) {
  if (!s || len == 0 || !needle || n_len == 0)
    return 0;

  size_t count = 0;
  const char *pos = s;
  size_t remaining = len;

  while (remaining > 0) {
    const char *found = gstrstr(pos, remaining, needle, n_len);
    if (!found)
      break;

    count++;
    size_t skip = (size_t)(found - pos) + n_len;
    if (skip > remaining)
      break;

    pos = found + n_len;
    remaining = len - (size_t)(pos - s);
  }

  return count;
}


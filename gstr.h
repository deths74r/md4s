/*
 * Finds the first occurrence of needle in haystack.
 * Returns a pointer to the match, or NULL if not found.
 */
static inline const char *gstrstr(const char *haystack, size_t h_len,
                                  const char *needle, size_t n_len) {
  if (!haystack || !needle)
    return NULL;
  if (n_len == 0)
    return haystack;
  if (h_len == 0)
    return NULL;

  size_t needle_graphemes = gstrlen(needle, n_len);
  if (needle_graphemes == 0)
    return haystack;

  size_t h_off = 0;

  while (h_off < h_len) {
    size_t h_pos = h_off;
    size_t n_pos = 0;
    int match = 1;
    size_t matched_graphemes = 0;

    while (matched_graphemes < needle_graphemes) {
      if (h_pos >= h_len) {
        match = 0;
        break;
      }

      int h_next = utf8_next_grapheme(haystack, h_len, h_pos);
      int n_next = utf8_next_grapheme(needle, n_len, n_pos);

      size_t h_glen = (h_next - h_pos);
      size_t n_glen = (n_next - n_pos);

      if (h_glen != n_glen ||
          memcmp(haystack + h_pos, needle + n_pos, h_glen) != 0) {
        match = 0;
        break;
      }

      h_pos = h_next;
      n_pos = n_next;
      matched_graphemes++;
    }

    if (match)
      return haystack + h_off;

    h_off = utf8_next_grapheme(haystack, h_len, h_off);
  }

  return NULL;
}


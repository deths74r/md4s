/* ============================================================================
 * Grapheme String Layer: Prefix/Suffix Functions
 * ============================================================================
 */

static inline int gstrstartswith(const char *s, size_t s_len,
                                 const char *prefix, size_t prefix_len) {
  if (!s || !prefix)
    return 0;
  if (prefix_len == 0)
    return 1;
  if (s_len < prefix_len)
    return 0;

  /* Compare grapheme by grapheme */
  size_t s_off = 0;
  size_t p_off = 0;

  while (p_off < prefix_len) {
    if (s_off >= s_len)
      return 0;

    int s_next = utf8_next_grapheme(s, s_len, s_off);
    int p_next = utf8_next_grapheme(prefix, prefix_len, p_off);

    size_t s_glen = (size_t)(s_next - s_off);
    size_t p_glen = (size_t)(p_next - p_off);

    if (s_glen != p_glen || memcmp(s + s_off, prefix + p_off, s_glen) != 0)
      return 0;

    s_off = s_next;
    p_off = p_next;
  }

  return 1;
}

/*
 * Returns 1 if the string ends with the given suffix, 0 otherwise.
 * Comparison is grapheme-by-grapheme.
 */
static inline int gstrendswith(const char *s, size_t s_len, const char *suffix,
                               size_t suffix_len) {
  if (!s || !suffix)
    return 0;
  if (suffix_len == 0)
    return 1;
  if (s_len < suffix_len)
    return 0;

  /* Count graphemes in both strings */
  size_t s_graphemes = gstrlen(s, s_len);
  size_t suffix_graphemes = gstrlen(suffix, suffix_len);

  if (suffix_graphemes > s_graphemes)
    return 0;

  /* Find offset to start comparison */
  size_t start_grapheme = s_graphemes - suffix_graphemes;
  size_t s_offset = gstroff(s, s_len, start_grapheme);

  /* Compare remaining graphemes */
  size_t s_off = (int)s_offset;
  size_t suf_off = 0;

  while (suf_off < suffix_len) {
    if (s_off >= s_len)
      return 0;

    int s_next = utf8_next_grapheme(s, s_len, s_off);
    int suf_next = utf8_next_grapheme(suffix, suffix_len, suf_off);

    size_t s_glen = (size_t)(s_next - s_off);
    size_t suf_glen = (size_t)(suf_next - suf_off);

    if (s_glen != suf_glen || memcmp(s + s_off, suffix + suf_off, s_glen) != 0)
      return 0;

    s_off = s_next;
    suf_off = suf_next;
  }

  return 1;
}

/* ============================================================================
 * Grapheme String Layer: Search Functions
 * ============================================================================
 */

static inline const char *gstrchr(const char *s, size_t len,
                                  const char *grapheme, size_t g_len) {
  if (!s || len == 0 || !grapheme || g_len == 0)
    return NULL;

  size_t offset = 0;

  while (offset < len) {
    size_t next = utf8_next_grapheme(s, len, offset);
    size_t grapheme_len = (next - offset);

    if (grapheme_len == g_len && memcmp(s + offset, grapheme, g_len) == 0) {
      return s + offset;
    }

    offset = next;
  }

  return NULL;
}


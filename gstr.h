/* ============================================================================
 * Grapheme String Layer: Extraction Functions
 * ============================================================================
 */

static inline size_t gstrsub(char *dst, size_t dst_size, const char *src,
                             size_t src_len, size_t start_grapheme,
                             size_t count) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0 || count == 0)
    return 0;

  size_t start_offset = gstroff(src, src_len, start_grapheme);
  if (start_offset >= src_len)
    return 0;

  size_t offset = start_offset;
  size_t graphemes_copied = 0;

  while (offset < src_len && graphemes_copied < count) {
    offset = utf8_next_grapheme(src, src_len, offset);
    graphemes_copied++;
  }

  size_t bytes_to_copy = offset - start_offset;

  if (bytes_to_copy >= dst_size) {
    int fit_offset = (int)start_offset;
    int last_complete = (int)start_offset;

    while ((size_t)fit_offset < start_offset + bytes_to_copy) {
      size_t next = utf8_next_grapheme(src, src_len, fit_offset);
      if ((size_t)(next - (int)start_offset) < dst_size) {
        last_complete = next;
      } else {
        break;
      }
      fit_offset = next;
    }
    bytes_to_copy = (size_t)(last_complete - (int)start_offset);
  }

  memcpy(dst, src + start_offset, bytes_to_copy);
  dst[bytes_to_copy] = '\0';

  return bytes_to_copy;
}

/* ============================================================================
 * Grapheme String Layer: Copy Functions
 * ============================================================================
 */

static inline size_t gstrcpy(char *dst, size_t dst_size, const char *src,
                             size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  size_t bytes_to_copy = src_len;
  if (bytes_to_copy >= dst_size)
    bytes_to_copy = dst_size - 1;

  size_t offset = 0;
  size_t last_complete = 0;

  while (offset < bytes_to_copy) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    if (next <= bytes_to_copy) {
      last_complete = next;
    } else {
      break;
    }
    offset = next;
  }

  memcpy(dst, src, (size_t)last_complete);
  dst[last_complete] = '\0';

  return (size_t)last_complete;
}

/*
 * Copies up to n grapheme clusters from src to dst.
 * Always null-terminates. Returns bytes written (excluding null).
 */
static inline size_t gstrncpy(char *dst, size_t dst_size, const char *src,
                              size_t src_len, size_t n) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0 || n == 0)
    return 0;

  size_t offset = 0;
  size_t count = 0;

  while (offset < src_len && count < n) {
    offset = utf8_next_grapheme(src, src_len, offset);
    count++;
  }

  size_t bytes_to_copy = offset;
  if (bytes_to_copy >= dst_size)
    bytes_to_copy = dst_size - 1;

  size_t fit_offset = 0;
  size_t last_complete = 0;

  while ((size_t)fit_offset < bytes_to_copy) {
    size_t next = utf8_next_grapheme(src, src_len, fit_offset);
    if (next <= bytes_to_copy) {
      last_complete = next;
    } else {
      break;
    }
    fit_offset = next;
  }

  memcpy(dst, src, (size_t)last_complete);
  dst[last_complete] = '\0';

  return (size_t)last_complete;
}

/* ============================================================================
 * Grapheme String Layer: Concatenation Functions
 * ============================================================================
 */

static inline size_t gstrcat(char *dst, size_t dst_size, const char *src,
                             size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  size_t dst_len = strlen(dst);
  if (dst_len >= dst_size - 1)
    return dst_len;

  if (!src || src_len == 0)
    return dst_len;

  size_t remaining = dst_size - dst_len - 1;
  size_t bytes_to_copy = src_len;
  if (bytes_to_copy > remaining)
    bytes_to_copy = remaining;

  size_t offset = 0;
  size_t last_complete = 0;

  while (offset < bytes_to_copy) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    if (next <= bytes_to_copy) {
      last_complete = next;
    } else {
      break;
    }
    offset = next;
  }

  memcpy(dst + dst_len, src, (size_t)last_complete);
  dst[dst_len + (size_t)last_complete] = '\0';

  return dst_len + (size_t)last_complete;
}

/*
 * Appends up to n grapheme clusters from src to dst.
 * Always null-terminates. Returns total bytes in dst.
 */
static inline size_t gstrncat(char *dst, size_t dst_size, const char *src,
                              size_t src_len, size_t n) {
  if (!dst || dst_size == 0)
    return 0;

  size_t dst_len = strlen(dst);
  if (dst_len >= dst_size - 1)
    return dst_len;

  if (!src || src_len == 0 || n == 0)
    return dst_len;

  size_t offset = 0;
  size_t count = 0;

  while (offset < src_len && count < n) {
    offset = utf8_next_grapheme(src, src_len, offset);
    count++;
  }

  size_t bytes_to_append = offset;
  size_t remaining = dst_size - dst_len - 1;
  if (bytes_to_append > remaining)
    bytes_to_append = remaining;

  size_t fit_offset = 0;
  size_t last_complete = 0;

  while ((size_t)fit_offset < bytes_to_append) {
    size_t next = utf8_next_grapheme(src, src_len, fit_offset);
    if (next <= bytes_to_append) {
      last_complete = next;
    } else {
      break;
    }
    fit_offset = next;
  }

  memcpy(dst + dst_len, src, (size_t)last_complete);
  dst[dst_len + (size_t)last_complete] = '\0';

  return dst_len + (size_t)last_complete;
}

/* ============================================================================
 * Grapheme String Layer: Allocation Functions
 * ============================================================================
 */

static inline char *gstrdup(const char *s, size_t len) {
  if (!s)
    return NULL;

  char *result = malloc(len + 1);
  if (!result)
    return NULL;

  memcpy(result, s, len);
  result[len] = '\0';
  return result;
}

/*
 * Duplicates up to n grapheme clusters from the source string.
 * Allocates memory that the caller must free.
 */
static inline char *gstrndup(const char *s, size_t len, size_t n) {
  if (!s)
    return NULL;

  if (n == 0)
    return gstrdup("", 0);

  size_t offset = 0;
  size_t count = 0;

  while (offset < len && count < n) {
    offset = utf8_next_grapheme(s, len, offset);
    count++;
  }

  return gstrdup(s, offset);
}

/* ============================================================================
 * Grapheme String Layer: Tokenization Functions
 * ============================================================================
 */

static inline const char *gstrsep(const char **stringp, size_t *lenp,
                                  const char *delim, size_t d_len,
                                  size_t *tok_len) {
  if (!stringp || !*stringp || !lenp)
    return NULL;

  if (*lenp == 0) {
    *stringp = NULL;
    return NULL;
  }

  const char *start = *stringp;
  size_t len = *lenp;

  if (!delim || d_len == 0) {
    if (tok_len)
      *tok_len = len;
    *stringp = NULL;
    *lenp = 0;
    return start;
  }

  size_t offset = 0;

  while (offset < len) {
    size_t next = utf8_next_grapheme(start, len, offset);
    size_t grapheme_len = (next - offset);

    if (gstr_grapheme_in_set(start + offset, grapheme_len, delim, d_len)) {
      if (tok_len)
        *tok_len = offset;
      *stringp = start + next;
      *lenp = len - next;
      return start;
    }
    offset = next;
  }

  if (tok_len)
    *tok_len = len;
  *stringp = NULL;
  *lenp = 0;
  return start;
}

/* ============================================================================
 * Grapheme String Layer: Trimming Functions
 * ============================================================================
 */

static inline size_t gstrltrim(char *dst, size_t dst_size, const char *src,
                               size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  size_t offset = 0;

  while (offset < src_len) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    size_t grapheme_len = (next - offset);

    if (!gstr_is_whitespace(src + offset, grapheme_len)) {
      break;
    }
    offset = next;
  }

  size_t remaining = src_len - offset;
  return gstrcpy(dst, dst_size, src + offset, remaining);
}

/*
 * Copies src to dst with trailing whitespace removed.
 * Returns bytes written (excluding null terminator).
 */
static inline size_t gstrrtrim(char *dst, size_t dst_size, const char *src,
                               size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  size_t last_non_ws_end = 0;
  size_t offset = 0;

  while (offset < src_len) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    size_t grapheme_len = (next - offset);

    if (!gstr_is_whitespace(src + offset, grapheme_len)) {
      last_non_ws_end = next;
    }
    offset = next;
  }

  return gstrcpy(dst, dst_size, src, (size_t)last_non_ws_end);
}

/*
 * Copies src to dst with leading and trailing whitespace removed.
 * Returns bytes written (excluding null terminator).
 */
static inline size_t gstrtrim(char *dst, size_t dst_size, const char *src,
                              size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  size_t start_offset = 0;

  while (start_offset < src_len) {
    size_t next = utf8_next_grapheme(src, src_len, start_offset);
    size_t grapheme_len = (next - start_offset);

    if (!gstr_is_whitespace(src + start_offset, grapheme_len)) {
      break;
    }
    start_offset = next;
  }

  if (start_offset >= src_len)
    return 0;

  size_t last_non_ws_end = start_offset;
  size_t trim_off = start_offset;

  while (trim_off < src_len) {
    size_t next = utf8_next_grapheme(src, src_len, trim_off);
    size_t grapheme_len = (next - trim_off);

    if (!gstr_is_whitespace(src + trim_off, grapheme_len)) {
      last_non_ws_end = next;
    }
    trim_off = next;
  }

  size_t trimmed_len = (size_t)last_non_ws_end - (size_t)start_offset;
  return gstrcpy(dst, dst_size, src + start_offset, trimmed_len);
}

/* ============================================================================
 * Grapheme String Layer: Transformation Functions
 * ============================================================================
 */

static inline size_t gstrrev(char *dst, size_t dst_size, const char *src,
                             size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  /* Pass 1 (forward): measure how many complete graphemes fit in dst */
  size_t total_bytes = 0;
  size_t n_graphemes = 0;
  size_t offset = 0;
  while (offset < src_len) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    size_t glen = (next - offset);
    if (total_bytes + glen >= dst_size)
      break;
    total_bytes += glen;
    n_graphemes++;
    offset = next;
  }

  if (n_graphemes == 0)
    return 0;

  /* Pass 2 (backward): copy graphemes in reverse order into dst.
   * Walk backward from the end of the measured region (total_bytes). */
  size_t written = 0;
  size_t current_end = (int)total_bytes;
  for (size_t i = 0; i < n_graphemes; i++) {
    int start = utf8_prev_grapheme(src, src_len, current_end);
    size_t glen = (current_end - start);
    memcpy(dst + written, src + start, glen);
    written += glen;
    current_end = start;
  }

  dst[written] = '\0';
  return written;
}

/*
 * Replaces all occurrences of old_str with new_str, writing to dst.
 * Returns bytes written. Preserves grapheme boundaries on truncation.
 */
static inline size_t gstrreplace(char *dst, size_t dst_size, const char *src,
                                 size_t src_len, const char *old_str,
                                 size_t old_len, const char *new_str,
                                 size_t new_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  if (!old_str || old_len == 0)
    return gstrcpy(dst, dst_size, src, src_len);

  size_t written = 0;
  const char *pos = src;
  size_t remaining = src_len;

  while (remaining > 0) {
    const char *found = gstrstr(pos, remaining, old_str, old_len);

    if (!found) {
      size_t to_copy = remaining;
      if (written + to_copy >= dst_size) {
        int fit_offset = 0;
        size_t last_complete = 0;
        size_t max_bytes = dst_size - written - 1;

        while ((size_t)fit_offset < to_copy && (size_t)fit_offset < max_bytes) {
          size_t next = utf8_next_grapheme(pos, (int)remaining, fit_offset);
          if (next <= max_bytes) {
            last_complete = next;
          } else {
            break;
          }
          fit_offset = next;
        }
        to_copy = (size_t)last_complete;
      }
      memcpy(dst + written, pos, to_copy);
      written += to_copy;
      break;
    }

    size_t before_len = (size_t)(found - pos);

    if (before_len > 0) {
      if (written + before_len >= dst_size) {
        int fit_offset = 0;
        size_t last_complete = 0;
        size_t max_bytes = dst_size - written - 1;

        while ((size_t)fit_offset < before_len &&
               (size_t)fit_offset < max_bytes) {
          size_t next = utf8_next_grapheme(pos, (int)before_len, fit_offset);
          if (next <= max_bytes) {
            last_complete = next;
          } else {
            break;
          }
          fit_offset = next;
        }
        memcpy(dst + written, pos, (size_t)last_complete);
        written += (size_t)last_complete;
        dst[written] = '\0';
        return written;
      }
      memcpy(dst + written, pos, before_len);
      written += before_len;
    }

    if (new_str && new_len > 0) {
      if (written + new_len >= dst_size) {
        int fit_offset = 0;
        size_t last_complete = 0;
        size_t max_bytes = dst_size - written - 1;

        while ((size_t)fit_offset < new_len && (size_t)fit_offset < max_bytes) {
          size_t next = utf8_next_grapheme(new_str, new_len, fit_offset);
          if (next <= max_bytes) {
            last_complete = next;
          } else {
            break;
          }
          fit_offset = next;
        }
        memcpy(dst + written, new_str, (size_t)last_complete);
        written += (size_t)last_complete;
        dst[written] = '\0';
        return written;
      }
      memcpy(dst + written, new_str, new_len);
      written += new_len;
    }

    pos = found + old_len;
    remaining = src_len - (size_t)(pos - src);
  }

  dst[written] = '\0';
  return written;
}

/* ============================================================================
 * Grapheme String Layer: Case Conversion Functions (ASCII only)
 * ============================================================================
 */

static inline size_t gstrlower(char *dst, size_t dst_size, const char *src,
                               size_t src_len) {
  if (!dst || dst_size == 0)
    return 0;

  dst[0] = '\0';

  if (!src || src_len == 0)
    return 0;

  size_t bytes_to_copy = src_len;
  if (bytes_to_copy >= dst_size)
    bytes_to_copy = dst_size - 1;

  /* Find last complete grapheme that fits */
  size_t offset = 0;
  size_t last_complete = 0;

  while (offset < bytes_to_copy) {
    size_t next = utf8_next_grapheme(src, src_len, offset);
    if (next <= bytes_to_copy) {
      last_complete = next;
    } else {
      break;
    }
    offset = next;
  }

  /* Copy and convert ASCII to lowercase */
  for (size_t i = 0; i < last_complete; i++) {
    dst[i] = gstr_ascii_lower(src[i]);
  }
  dst[last_complete] = '\0';

  return (size_t)last_complete;
}


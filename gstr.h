/*
 * Checks whether a Unicode codepoint falls within any of the given ranges
 * using binary search.
 */
static inline int unicode_range_contains(uint32_t codepoint,
                                  const struct unicode_range *ranges,
                                  int count) {
  int low = 0;
  int high = count - 1;
  while (low <= high) {
    int mid = (low + high) / 2;
    if (codepoint < ranges[mid].start) {
      high = mid - 1;
    } else if (codepoint > ranges[mid].end) {
      low = mid + 1;
    } else {
      return 1;
    }
  }
  return 0;
}

/*
 * Determines the Grapheme Cluster Break (GCB) property for a codepoint.
 */
static inline enum gcb_property get_gcb(uint32_t cp) {
  /* Hangul syllables: LV and LVT are computed algorithmically */
  if (cp >= HANGUL_SBASE && cp <= HANGUL_SEND) {
    return ((cp - HANGUL_SBASE) % HANGUL_TCOUNT == 0) ? GCB_LV : GCB_LVT;
  }

  /* Binary search in property table */
  size_t low = 0;
  size_t high = GCB_RANGE_COUNT;
  while (low < high) {
    size_t mid = low + (high - low) / 2;
    if (cp < GCB_RANGES[mid].start) {
      high = mid;
    } else if (cp > GCB_RANGES[mid].end) {
      low = mid + 1;
    } else {
      return (enum gcb_property)GCB_RANGES[mid].property;
    }
  }
  return GCB_OTHER;
}

/*
 * Checks if a codepoint is an "Extended Pictographic" character.
 */
static inline int is_extended_pictographic(uint32_t cp) {
  return unicode_range_contains(cp, EXTENDED_PICTOGRAPHIC_RANGES,
                                EXTENDED_PICTOGRAPHIC_COUNT);
}

/*
 * Checks if a codepoint is an Indic Conjunct Break (InCB) Linker character.
 */
static inline int is_incb_linker(uint32_t cp) {
  size_t low = 0;
  size_t high = INCB_LINKER_COUNT;
  while (low < high) {
    size_t mid = low + (high - low) / 2;
    if (cp < INCB_LINKERS[mid]) {
      high = mid;
    } else if (cp > INCB_LINKERS[mid]) {
      low = mid + 1;
    } else {
      return 1;
    }
  }
  return 0;
}

/*
 * Checks if a codepoint is an Indic Conjunct Break (InCB) Consonant.
 */
static inline int is_incb_consonant(uint32_t cp) {
  return unicode_range_contains(cp, INCB_CONSONANTS, INCB_CONSONANT_COUNT);
}

/*
 * Determine if there's a grapheme cluster break between two codepoints.
 * Implements UAX #29 extended grapheme cluster rules.
 */
static inline int is_grapheme_break(enum gcb_property prev_prop,
                             enum gcb_property curr_prop, int ri_count,
                             int in_ext_pict, uint32_t curr_cp,
                             int incb_state) {
  /* GB3: CR × LF */
  if (prev_prop == GCB_CR && curr_prop == GCB_LF) {
    return 0;
  }

  /* GB4: (Control | CR | LF) ÷ */
  if (prev_prop == GCB_CONTROL || prev_prop == GCB_CR || prev_prop == GCB_LF) {
    return 1;
  }

  /* GB5: ÷ (Control | CR | LF) */
  if (curr_prop == GCB_CONTROL || curr_prop == GCB_CR || curr_prop == GCB_LF) {
    return 1;
  }

  /* GB6: L × (L | V | LV | LVT) */
  if (prev_prop == GCB_L && (curr_prop == GCB_L || curr_prop == GCB_V ||
                             curr_prop == GCB_LV || curr_prop == GCB_LVT)) {
    return 0;
  }

  /* GB7: (LV | V) × (V | T) */
  if ((prev_prop == GCB_LV || prev_prop == GCB_V) &&
      (curr_prop == GCB_V || curr_prop == GCB_T)) {
    return 0;
  }

  /* GB8: (LVT | T) × T */
  if ((prev_prop == GCB_LVT || prev_prop == GCB_T) && curr_prop == GCB_T) {
    return 0;
  }

  /* GB9: × (Extend | ZWJ) */
  if (curr_prop == GCB_EXTEND || curr_prop == GCB_ZWJ) {
    return 0;
  }

  /* GB9a: × SpacingMark */
  if (curr_prop == GCB_SPACING_MARK) {
    return 0;
  }

  /* GB9b: Prepend × */
  if (prev_prop == GCB_PREPEND) {
    return 0;
  }

  /* GB9c: Indic conjunct sequences */
  if (incb_state == 2 && is_incb_consonant(curr_cp)) {
    return 0;
  }

  /* GB11: ExtPict Extend* ZWJ × ExtPict */
  if (in_ext_pict && prev_prop == GCB_ZWJ &&
      is_extended_pictographic(curr_cp)) {
    return 0;
  }

  /* GB12/GB13: RI × RI (only for pairs) */
  if (prev_prop == GCB_REGIONAL_INDICATOR &&
      curr_prop == GCB_REGIONAL_INDICATOR) {
    return (ri_count % 2) == 0;
  }

  /* GB999: Any ÷ Any */
  return 1;
}

